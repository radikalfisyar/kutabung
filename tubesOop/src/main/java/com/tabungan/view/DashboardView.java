    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTabungan.getSelectedRow(); // Cek baris yang dipilih
    if (selectedRow != -1) { // Jika ada baris yang dipilih
        String namaTabungan = tblTabungan.getValueAt(selectedRow, 0).toString(); // Ambil nama tabungan
        
        // Konfirmasi penghapusan
        int response = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus tabungan " + namaTabungan + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION) {
            // Panggil metode hapus tabungan di DashboardController
            DashboardController dashboardController = new DashboardController();
            boolean isDeleted = dashboardController.hapusTabungan(namaTabungan);
            
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Tabungan berhasil dihapus!");
                refresh(); // Refresh tabel setelah berhasil hapus
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus tabungan!");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih tabungan yang ingin dihapus!");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

