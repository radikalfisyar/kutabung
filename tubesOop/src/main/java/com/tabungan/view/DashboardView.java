    public void loadDataToTable(DashboardController dashboardController, String username) {
        // Ambil model dari tabel
        DefaultTableModel model = (DefaultTableModel) tblTabungan.getModel();

        // Hapus semua data yang ada di tabel (jika ada)
        model.setRowCount(0);

        // Ambil data tabungan dari controller berdasarkan username
        List<Dashboard> daftarTabungan = dashboardController.ambilSemuaTabungan(username);

        // Tambahkan setiap data tabungan ke tabel
        for (Dashboard dashboard : daftarTabungan) {
            // Hitung persentase pencapaian
            double persentase = (dashboard.getTotalTerkumpul() / dashboard.getTargetTabungan()) * 100;

            // Tambahkan baris baru ke tabel
            model.addRow(new Object[]{
                dashboard.getNamaTabungan(), // Nama Tabungan
                dashboard.getTargetTabungan(), // Target
                dashboard.getTotalTerkumpul(), // Total Terkumpul
                String.format("%.2f%%", persentase) // Pencapaian dalam persen
            });
        }
    }

    private String getSelectedNamaTabungan() {
        int selectedRow = tblTabungan.getSelectedRow();
        if (selectedRow != -1) {
            return tblTabungan.getValueAt(selectedRow, 0).toString(); // Kolom 0 = Nama Tabungan
        }
        JOptionPane.showMessageDialog(this, "Silakan pilih tabungan yang ingin diupdate!");
        return null;
    }

