    private void btnAddNominalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNominalActionPerformed
       // Ambil baris yang dipilih dari tabel
    int row = tblTabungan.getSelectedRow();

    if (row >= 0) {
        // Ambil nama tabungan dari tabel
        String namaTabungan = (String) tblTabungan.getValueAt(row, 0);

        try {
            // Ambil nominal dari input pengguna
            double nominal = Double.parseDouble(txtNominal.getText());

            if (nominal <= 0) {
                JOptionPane.showMessageDialog(this,
                        "Nominal harus lebih besar dari 0.",
                        "Kesalahan Input",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Tambahkan nominal melalui controller
            DashboardController dashboardController = new DashboardController();
            boolean berhasil = dashboardController.tambahNominal(namaTabungan, nominal);

            if (berhasil) {
                JOptionPane.showMessageDialog(this,
                        "Nominal berhasil ditambahkan!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);

                // Update nilai total terkumpul di model tabel secara langsung
                double totalTerkumpul = (double) tblTabungan.getValueAt(row, 2); // Kolom 2 = Total Terkumpul
                totalTerkumpul += nominal; // Tambahkan nominal ke total terkumpul

                tblTabungan.setValueAt(totalTerkumpul, row, 2); // Update model tabel
                
                // Hitung persentase pencapaian dan update kolom pencapaian
                double targetTabungan = (double) tblTabungan.getValueAt(row, 1); // Kolom 1 = Target
                double persentase = (totalTerkumpul / targetTabungan) * 100;
                tblTabungan.setValueAt(String.format("%.2f%%", persentase), row, 3); // Kolom 3 = Pencapaian
                
                txtNominal.setText(""); // Bersihkan field nominal
            } else {
                JOptionPane.showMessageDialog(this,
                        "Gagal menambahkan nominal. Silakan coba lagi.",
                        "Kesalahan",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Masukkan nominal dalam format angka yang valid.",
                    "Kesalahan Input",
                    JOptionPane.WARNING_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this,
                "Pilih salah satu tabungan dari tabel untuk menambahkan nominal.",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnAddNominalActionPerformed

