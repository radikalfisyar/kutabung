    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblTabungan.getSelectedRow(); // Cek baris yang dipilih di tabel
    if (selectedRow != -1) { // Jika ada baris yang dipilih
        String oldNamaTabungan = tblTabungan.getValueAt(selectedRow, 0).toString(); // Kolom 0 = Nama Tabungan
        String newNamaTabungan = txtNamaTabunganBaru.getText(); // Ambil input dari txtNamaTabunganBaru
        String targetString = txtTargetTabunganBaru.getText(); // Ambil input dari txtTargetTabunganBaru

        if (newNamaTabungan.isEmpty() || targetString.isEmpty()) { // Validasi input tidak boleh kosong
            JOptionPane.showMessageDialog(this, "Nama tabungan dan target tidak boleh kosong!");
            return;
        }

        try {
            double newTargetTabungan = Double.parseDouble(targetString); // Konversi target menjadi double

            // Buat instance dari DashboardController
            DashboardController dashboardController = new DashboardController();
            boolean isUpdated = dashboardController.updateTabungan(oldNamaTabungan, newNamaTabungan, newTargetTabungan);

            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Tabungan berhasil diperbarui!");
                refresh(); // Refresh tabel setelah berhasil update
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui tabungan!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Target tabungan harus berupa angka!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih tabungan yang ingin diupdate!");
    }
    
    }//GEN-LAST:event_btnUpdateActionPerformed

